using static Coal.Menu.Main;
using static Coal.Settings;

namespace Coal.Mods
{
    internal class SettingsMods
    {
        public static void EnterSettings()
        {
            buttonsType = 1;
        }

        public static void MenuSettings()
        {
            buttonsType = 2;
        }

        public static void MovementSettings()
        {
            buttonsType = 3;
        }

        public static void ProjectileSettings()
        {
            buttonsType = 4;
        }

        public static void MovementMods()
        {
            buttonsType = 5;
        }

        public static void RightHand()
        {
            rightHanded = true;
        }

        public static void LeftHand()
        {
            rightHanded = false;
        }

        public static void EnableFPSCounter()
        {
            fpsCounter = true;
        }

        public static void DisableFPSCounter()
        {
            fpsCounter = false;
        }

        public static void EnableNotifications()
        {
            disableNotifications = false;
        }

        public static void DisableNotifications()
        {
            disableNotifications = true;
        }

        public static void EnableDisconnectButton()
        {
            disconnectButton = true;
        }

        public static void DisableDisconnectButton()
        {
            disconnectButton = false;
        }

        public static int currentSpeed = 2;
        public static void ToggleSpeed()
        {
            if (currentSpeed == 3)
            {
                currentSpeed = 0;
            } else
            {
                currentSpeed++;
            }
        }

        public static string GetSpeed()
        {
            SpeedTypes speedName = (SpeedTypes)currentSpeed;
            return speedName.ToString();
        }
    }
}
